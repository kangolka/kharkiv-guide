    // ========== ЗАСТОСУВАТИ МОВУ ДО DOM ==========
    function applyLanguage() {
        // data-i18n
        document.querySelectorAll('[data-i18n]').forEach(function (el) {
            var key = el.getAttribute('data-i18n');
            var val = t(key);
            if (val) el.textContent = val;
        });

        // data-i18n-placeholder
        document.querySelectorAll('[data-i18n-placeholder]').forEach(function (el) {
            var key = el.getAttribute('data-i18n-placeholder');
            var val = t(key);
            if (val) el.setAttribute('placeholder', val);
        });

        // Фільтри
        document.querySelectorAll('[data-i18n-filter]').forEach(function (el) {
            var key = el.getAttribute('data-i18n-filter');
            var val = t(key);
            if (val) el.textContent = val;
        });

        // html lang
        document.documentElement.lang = currentLang === 'ru' ? 'ru' : currentLang === 'en' ? 'en' : 'uk';

        // page title
        var title = t('pageTitle');
        if (title) document.title = title;

        // Оновити активну кнопку мови
        document.querySelectorAll('.lang-btn').forEach(function (btn) {
            btn.classList.toggle('active', btn.getAttribute('data-lang') === currentLang);
        });

        // Перемалювати картки
        renderPlaces(getCurrentPlaces());

        // Оновити пошук
        applyFilters();

        // Перемалювати карту
        initMap();

        // Зберегти
        try { localStorage.setItem('kharkivLang', currentLang); } catch(e) { /* ignore */ }
    }

    // ========== RENDER PLACES ==========
    function renderPlaces(places) {
        var grid = document.getElementById('placesGrid');
        if (!grid) return;

        if (places.length === 0) {
            grid.innerHTML = '<div class="no-results">' +
                '<div class="no-results-icon">\uD83D\uDD0D</div>' +
                '<p>' + escapeHtml(t('noResults')) + '</p>' +
                '</div>';
            return;
        }

        var html = places.map(function (place) {
            var cat = CATEGORY_CONFIG[place.category] || { color: '#6B7280', icon: '\uD83D\uDCCD' };
            var safeColor = isValidHex(cat.color) ? cat.color : '#6B7280';
            var safeName = escapeHtml(place.name);
            var safeCategory = escapeHtml(place.category);
            var safeShortDesc = escapeHtml(place.shortDesc);
            var safeFullDesc = escapeHtml(place.fullDesc);
            var safeIcon = cat.icon;
            var encodedName = encodeURIComponent(place.name);
            var hexColor = safeColor.replace('#', '');

            // Фото з кешу або placeholder
            var images = IMAGE_CACHE[place.id] || [];
            var img1 = images[0] || null;
            var img2 = images[1] || null;
            var bgColor = getPlaceholderColor(place.id);
            var bgColorHex = bgColor.replace('#', '');

            var galleryHtml = '<div class="gallery" data-place-id="' + place.id + '">';
            galleryHtml += '<div class="gallery-track">';

            if (img1) {
                galleryHtml += '<img src="' + img1 + '" alt="" loading="lazy" crossorigin="anonymous" onerror="this.onerror=null;this.src=\'https://placehold.co/640x400/' + bgColorHex + '/ffffff\';">';
            } else {
                galleryHtml += '<div class="gallery-placeholder" style="background:' + bgColor + ';"></div>';
            }

            if (img2) {
                galleryHtml += '<img src="' + img2 + '" alt="" loading="lazy" crossorigin="anonymous" onerror="this.style.display=\'none\';">';
            }

            galleryHtml += '</div>';
            galleryHtml += '<div class="gallery-dots">';
            var count = (img1 && img2) ? 2 : 1;
            for (var i = 0; i < count; i++) {
                galleryHtml += '<span class="gallery-dot' + (i === 0 ? ' active' : '') + '" data-index="' + i + '" data-place="' + place.id + '"></span>';
            }
            galleryHtml += '</div>';
            if (count > 1) {
                galleryHtml += '<button class="gallery-nav gallery-prev" data-place="' + place.id + '" aria-label="Previous">\u2039</button>';
                galleryHtml += '<button class="gallery-nav gallery-next" data-place="' + place.id + '" aria-label="Next">\u203A</button>';
            }
            galleryHtml += '</div>';

            return '<article class="place-card animate-on-scroll" data-category="' + safeCategory + '" data-id="' + place.id + '">' +
                '<div class="place-image">' +
                    galleryHtml +
                    '<span class="place-badge" style="background:' + safeColor + '">' + safeIcon + ' ' + safeCategory + '</span>' +
                '</div>' +
                '<div class="place-content">' +
                    '<h3 class="place-title">' + safeName + '</h3>' +
                    '<p class="place-desc">' + safeShortDesc + '</p>' +
                    '<div class="place-full-desc" id="full-' + place.id + '">' +
                        '<p>' + safeFullDesc + '</p>' +
                    '</div>' +
                    '<button class="place-btn" data-place-id="' + place.id + '">' + escapeHtml(t('btnDetail')) + '</button>' +
                '</div>' +
                '</article>';
        }).join('');

        grid.innerHTML = html;
        observeElements();
    }


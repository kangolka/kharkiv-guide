    // ========== ФІЛЬТРАЦІЯ ==========
    function applyFilters() {
        var cards = document.querySelectorAll('.place-card');
        var visibleCount = 0;
        var places = getCurrentPlaces();

        cards.forEach(function (card) {
            var category = card.getAttribute('data-category');
            var placeId = parseInt(card.getAttribute('data-id'), 10);
            var place = places.find(function (p) { return p.id === placeId; });
            if (!place) return;

            var matchesCategory = activeCategory === 'all' || category === activeCategory;
            var matchesSearch = searchQuery === '' ||
                place.name.toLowerCase().indexOf(searchQuery) !== -1 ||
                place.shortDesc.toLowerCase().indexOf(searchQuery) !== -1 ||
                place.fullDesc.toLowerCase().indexOf(searchQuery) !== -1 ||
                place.category.toLowerCase().indexOf(searchQuery) !== -1;

            if (matchesCategory && matchesSearch) {
                card.classList.remove('hidden', 'fade-out');
                visibleCount++;
            } else {
                card.classList.add('fade-out');
                setTimeout(function () {
                    card.classList.add('hidden');
                }, 400);
            }
        });

        setTimeout(function () {
            var grid = document.getElementById('placesGrid');
            var noResults = grid ? grid.querySelector('.no-results') : null;
            if (visibleCount === 0 && !noResults) {
                grid.innerHTML = '<div class="no-results">' +
                    '<div class="no-results-icon">\uD83D\uDD0D</div>' +
                    '<p>' + escapeHtml(t('noResults')) + '</p>' +
                    '</div>';
            } else if (visibleCount > 0 && noResults) {
                noResults.remove();
            }
        }, 450);
    }

    // ========== КАРТА ==========
    function initMap() {
        var mapEl = document.getElementById('map');
        if (!mapEl) return;

        // If already initialized, just update markers
        if (mapInstance) {
            // Remove old markers
            mapMarkers.forEach(function (m) { mapInstance.removeLayer(m); });
            mapMarkers = [];
        } else {
            if (typeof L === 'undefined') return;
            mapInstance = L.map('map').setView([49.9935, 36.2304], 13);
            L.tileLayer('https://{s}.tile.openstreetmap.org/{z}/{x}/{y}.png', {
                attribution: '&copy; <a href="https://www.openstreetmap.org/copyright" target="_blank" rel="noopener noreferrer">OpenStreetMap</a> contributors',
                maxZoom: 19
            }).addTo(mapInstance);
        }

        var places = getCurrentPlaces();

        places.forEach(function (place) {
            var cat = CATEGORY_CONFIG[place.category] || { color: '#6B7280', icon: '\uD83D\uDCCD' };
            var safeColor = isValidHex(cat.color) ? cat.color : '#6B7280';

            var customIcon = L.divIcon({
                html: '<div style="' +
                    'background: ' + safeColor + ';' +
                    'width: 36px;' +
                    'height: 36px;' +
                    'border-radius: 50% 50% 50% 0;' +
                    'transform: rotate(-45deg);' +
                    'display: flex;' +
                    'align-items: center;' +
                    'justify-content: center;' +
                    'box-shadow: 0 2px 8px rgba(0,0,0,0.3);' +
                    'border: 2px solid white;' +
                    '"><span style="transform: rotate(45deg); font-size: 16px;">' + cat.icon + '</span></div>',
                iconSize: [36, 36],
                iconAnchor: [18, 36],
                popupAnchor: [0, -36],
                className: ''
            });

            var safeName = escapeHtml(place.name);
            var safeDesc = escapeHtml(place.shortDesc);
            var safeBtnText = escapeHtml(t('mapShowBtn'));

            var marker = L.marker([place.lat, place.lng], { icon: customIcon }).addTo(mapInstance);

            var popupContent = '<div class="map-popup">' +
                '<h4>' + safeName + '</h4>' +
                '<p>' + safeDesc + '</p>' +
                '<button class="map-popup-btn" data-place-id="' + place.id + '">' + safeBtnText + '</button>' +
                '</div>';

            marker.bindPopup(popupContent);
            mapMarkers.push(marker);
        });
    }

    function scrollToPlace(id) {
        var places = getCurrentPlaces();
        var place = places.find(function (p) { return p.id === id; });
        if (!place) return;

        var card = document.querySelector('.place-card[data-id="' + id + '"]');
        if (card) {
            card.classList.remove('hidden', 'fade-out');
        }

        document.querySelectorAll('.place-card').forEach(function (c) {
            c.classList.remove('hidden', 'fade-out');
        });

        activeCategory = 'all';
        searchQuery = '';

        var input = document.getElementById('searchInput');
        if (input) input.value = '';

        document.querySelectorAll('.filter-btn').forEach(function (btn) {
            btn.classList.remove('active');
        });

        var allBtn = document.querySelector('.filter-btn[data-category="all"]');
        if (allBtn) allBtn.classList.add('active');

        if (card) {
            card.scrollIntoView({ behavior: 'smooth', block: 'center' });
            card.style.boxShadow = '0 0 0 3px var(--accent)';
            setTimeout(function () {
                card.style.boxShadow = '';
            }, 2000);
        }
    }

    // ========== INTERSECTION OBSERVER ==========
    function observeElements() {
        var observer = new IntersectionObserver(function (entries) {
            entries.forEach(function (entry) {
                if (entry.isIntersecting) {
                    entry.target.classList.add('visible');
                    observer.unobserve(entry.target);
                }
            });
        }, {
            threshold: 0.1,
            rootMargin: '0px 0px -50px 0px'
        });

        document.querySelectorAll('.animate-on-scroll:not(.visible)').forEach(function (el) {
            observer.observe(el);
        });
    }


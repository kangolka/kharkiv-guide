    var IMAGE_CACHE = {};

    // Завантажити фото з Wikipedia REST API
    function fetchWikiImage(lang, title) {
        if (!title) return Promise.resolve(null);
        var url = 'https://' + lang + '.wikipedia.org/api/rest_v1/page/summary/' + encodeURIComponent(title);
        return fetch(url, { headers: { 'Accept': 'application/json' } })
            .then(function (res) {
                if (!res.ok) return null;
                return res.json();
            })
            .then(function (data) {
                if (data && data.thumbnail && data.thumbnail.source) {
                    // Повертаємо URL без зміни — Wikipedia вже дає правильний розмір
                    var src = data.thumbnail.source;
                    // Замінюємо 250px на 640px для кращої якості
                    if (src.indexOf('/250px-') !== -1) {
                        src = src.replace('/250px-', '/640px-');
                    }
                    return src;
                }
                return null;
            })
            .catch(function () { return null; });
    }

